fruits = ['banana', 'orange', 'mango', 'lemon']
fruits_copy = fruits.copy()
print(fruits_copy)       # ['banana', 'orange', 'mango', 'lemon']
print(fruits)

extended_fruits = fruits + fruits_copy
print(extended_fruits)

