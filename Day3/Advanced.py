# calculating area, volume, circumfrence, density etc.

# Area of a circle
radius = 7
area_of_a_circle = 3.14 * radius ** 2
print('Area of a cirle:' , area_of_a_circle)

# Area of a  rectangle
width = 7
height = 8
area_of_a_rectangle = width * height
print('Area of a Rectangle:', area_of_a_rectangle)

# Calculating weight of an object
mass = 80
gravity = 9.81
weight = mass * gravity
print ('Weight', weight, 'N')

# Density  of a liquid

mass = 80 # in Kg
volume = 0.08 # in M3
density = mass / volume # Kg/M3
print('Density:', density, 'Kg/m3')

